console.log("var example")
var a = 10; 
console.log(a);
{
    var a=20;
    console.log(a);
}
console.log(a);

// var is a variable whose value can be reinitialised, it has global scope

console.log("let example")
let b=10;
console.log(b);
{
    let b = 20;
    console.log(b);

}
console.log(b);


// let has local scope, it cannot be reinitialised

